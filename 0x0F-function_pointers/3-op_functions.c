#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the products of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the reuslt of division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result of the division  the integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - returns remainder of the division of a and b
 *@a: first integer
 *@b: second integer
 *
 * Return: remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}


